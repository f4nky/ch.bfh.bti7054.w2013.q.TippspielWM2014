<h1>Spielregeln</h1>
<h2>Regeln Tippspiel</h2>
<ol>
	<li>Anmelden/Nachrichten
		<ul>
			<li>Jeder Teilnehmer muss sich vorgängig mit seiner persönlichen E-Mail-Adresse registrieren</li>
			<li>Allfällige Nachrichten werden an die eingetragene E-Mail-Adresse versandt</li>
		</ul>
	</li>
	<li>Tippabgabe
		<ul>
			<li>Die Tippabgabe kann nur online erfolgen</li>
			<li>Die Spezialtipps sind vor Anpfiff des Eröffnungsspiels zu tippen</li>
			<li>Die Spiele der Gruppenphase sind vor Anpfiff des Eröffnungsspiels zu tippen</li>
			<li>Die Finalspiele sind jeweils vor dem Anpfiff des ersten Spiels der entsprechenden Runde (Achtel-, Viertel-, Halbfinal) zu tippen</li>
			<li>Das Spiel um Platz 3 sowie das Finalspiel sind jeweils vor deren Anpfiff zu tippen</li>
			<li>Nach abgelaufener Frist werden keine Resultate mehr entgegen genommen. Die verpasste Tippmöglichkeit verfällt.</li>
		</ul>
	</li>
	<li>Bewertung
		<ul>
			<li>Die Punktevergabe erfolgt nach Abpfiff der Spiele (bei Finalspielen inkl. allfälliger/m Verlängerung/ Penaltyschiessen)</li>
			<li>Die Tore einer/m Verlängerung/Penaltyschiessen werden berücksichtigt und bei der Bewertung berücksichtigt (auch beim Spezialtipp „Anzahl Tore“!)</li>
		</ul>
	</li>
	<li>System
		<ul>
			<li>Für etwaige Übertragungsfehler und sonstige technische Schwierigkeiten wird keine Verantwortung übernommen</li>
			<li>Sollte die Tippabgabe aus technischen Gründen nicht möglich sein, Tipp per E-Mail/WhatsApp/SMS an einen Administrator senden</li>
		</ul>
	</li>
	<li>Sonstiges
		<ul>
			<li>Die Eingabe wird zu den entsprechenden Zeitpunkten automatisch deaktiviert</li>
			<li>Es werden vom System keinerlei Erinnerungsnachrichten für noch offene Tipps versandt</li>
			<li>Die Eingabe der Spielresultate erfolgt manuell durch Administratoren, deshalb kann es zu Verzögerungen bei der Aktualisierung der Daten kommen</li>
		</ul>
	</li>
</ol>
